package com.vtr.habilidades.habilidades.acrobatics;

public class AcrobaticsFallExperience {

	private int distance;
	
	private double xp;

	public AcrobaticsFallExperience(int distance, double xp) {
		this.distance = distance;
		this.xp = xp;
	}
	
	public int getDistance() {
		return distance;
	}
	
	public double getXp() {
		return xp;
	}
}
